// Inlines marketing/*.md into src/lib/marketing/content.ts so the loader
// has zero filesystem dependencies at runtime — works on Vercel without
// any "include outside files" toggle, on Hugging Face without bind mounts,
// anywhere a Next.js bundle can run.
//
// Run via:  npm run sync-marketing
// Auto-runs on:  npm run dev  (predev) and  npm run build (prebuild)
//
// Source-of-truth MDs live at <repo>/marketing/*.md. Edit them there.

import { readFile, writeFile, mkdir } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC_DIR = path.resolve(__dirname, "..", "..", "marketing");
const OUT_FILE = path.resolve(__dirname, "..", "src", "lib", "marketing", "content.ts");

const FILES = [
  "06-keyword-research.md",
  "09-budget.md",
  "10-competitive-analysis.md",
  "11-kpi-tracker.md",
];

if (!existsSync(SRC_DIR)) {
  console.warn(
    `[sync-marketing] Source dir ${SRC_DIR} not found. ` +
      `Keeping existing ${OUT_FILE} — this is fine on Vercel where the source ` +
      `lives outside the deployed root.`,
  );
  process.exit(0);
}

const entries = [];
for (const f of FILES) {
  const txt = await readFile(path.join(SRC_DIR, f), "utf8");
  // Escape so it can sit inside a template literal:
  //   1. Backslashes first (avoid double-escape)
  //   2. Backticks
  //   3. ${ ... } interpolations
  const escaped = txt
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
  entries.push(`  ${JSON.stringify(f)}: \`${escaped}\``);
}

const out = `// AUTO-GENERATED by frontend/scripts/sync-marketing.mjs
// Source: <repo>/marketing/*.md  ·  Re-run: npm run sync-marketing
//
// Don't edit this file directly — edit the source MDs and re-sync.

/* eslint-disable */

export const RAW_MD: Record<string, string> = {
${entries.join(",\n")},
};
`;

await mkdir(path.dirname(OUT_FILE), { recursive: true });
await writeFile(OUT_FILE, out, "utf8");
console.log(
  `[sync-marketing] wrote ${path.relative(process.cwd(), OUT_FILE)} ` +
    `(${FILES.length} files, ${(Buffer.byteLength(out, "utf8") / 1024).toFixed(1)} KB)`,
);
